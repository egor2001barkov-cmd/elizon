export const cylinderLandingSlugs: string[] = cylinderLandings.map((l) => l.slug);

export function getFooterCylinders(): { href: string; label: string; title: string }[] {
  return cylinderLandings.map((l) => ({
    href: `/${l.slug}`,
    label: l.primaryKeyword ?? l.h1.slice(0, 36),
    title: l.title,
  }));
}

export function getCylinderModelLinks(): { href: string; label: string; km: number }[] {
  return cylinderModelsSeo.map((m) => ({
    href: `/catalog/optovolokonnye-cilindry/fo-0-25/${m.slug}`,
    label: m.modelCode,
    km: m.km,
  }));
}