export const applicationLandingSlugs: string[] = applicationLandings.map((l) => l.slug);

export function getApplicationLandingById(id: string): LandingPage | undefined {
  return applicationLandings.find((l) => l.applicationId === id);
}

export function getApplicationLandingBySlug(slug: string): LandingPage | undefined {
  return applicationLandings.find((l) => l.slug === slug);
}

export function getFooterApplications(): { href: string; label: string; title: string }[] {
  return applicationLandings.map((l) => ({
    href: applicationPath(l.slug),
    label: l.primaryKeyword ?? l.h1.slice(0, 36),
    title: l.title,
  }));
}

export function getApplicationNavChildren(): { href: string; label: string; title: string }[] {
  return applications.map((app) => {
    const landing = getApplicationLandingById(app.id);
    return {
      href: applicationPath(app.slug),
      label: app.navLabel,
      title: landing?.title ?? app.title,
    };
  });
}
